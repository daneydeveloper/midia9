                <h3 class="bs-title">Os benefícios da redes de afiliados</h3>
                <div class="bs-desc">
                    <p>Diferente das estratégias de marketing mencionadas acima, o mercado de afiliados tem como seu grande diferencial a forma de custeio. Todo investimento é baseado em uma comissão sobre a venda, isto é, não há nenhum risco de se investir na estratégia de afiliados e obter prejuízos. O anunciante que decidir por essa estratégia só pagará ao fornecedor se o mesmo trouxer venda a ele. Normalmente, é estabelecido uma média de 8% sobre a venda realizada, mas esses valores são facilmente negociados de acordo com o segmento de cada anunciante.</p>
                    <p>Geralmente, os fornecedores desse mercado são as redes de afiliados. Essas são plataformas que reúnem bases gigantescas de sites parceiros divididos em diversas categorias e que são responsáveis pela geração do tráfego e venda dos produtos e/ou serviços dos anunciantes. Esses parceiros podem ser blogueiros e influenciadores, sites de cupons e promoções, ou até mesmo soluções de tecnologia que trabalham com a otimização do tráfego do anunciante, tais como diminuição do bounce rate e recuperadores de carrinhos abandonados.</p>
                    <p>As redes atuam como um intermediário entre o site de e-commerce (anunciante) e os afiliados (geradores do tráfego). As mesmas são responsáveis por toda a tecnologia envolvida na operação, divulgação das ofertas do anunciante, pagamentos para cada afiliado, entre outros. Em um breve resumo da operação, o anunciante “X” encaminha as suas principais ofertas para a rede de afiliados e a mesma se compromete em cadastrá-las em sua plataforma, divulgá-las para a base de afiliados, fazê-los aderir às campanhas, muní-los de reports e, por fim, remunerá-los pelas vendas realizadas.</p>
                    <p>Porém, um assunto que vem tomando bastante espaço nas discussões sobre a estratégia de afiliação no e-commerce é a transparência. Grande parte das maiores redes de afiliados do Brasil não oferece abertamente e claramente a informação de quem é o afiliado e qual a estratégia do mesmo para realizar as vendas obtidas.  Sem ter essa visão clara, gerentes de marketing dos e-commerces perdem a possibilidade de desenhar as melhores estratégias para atingirem a performance no canal.</p>
                    <p>Deste modo, fica claro a importância de escolher, como parceiro, as redes que ofereçam reports robustos, claros e transparentes. E ainda, selecionar aquelas que possuem histórico e reconhecimento pela qualidade de suas tecnologias, seriedade e honestidade para com o mercado.</p>
                    <p>*Esta matéria foi escrita pela Rakuten Marketing Afiliados, empresa pioneira no mercado do afiliação global. Para se tornar um afiliado ou anunciante, clique aqui e acesse.</p>
