                    <h3 class="bs-title">Por onde posso começar a construir a audiência digital da minha empresa?</h3>
                    <p>Agora que você possui dois princípios básicos para construir sua audiência digital ou até mesmo aumentar sua presença online, vamos dar uma olhada nos principais canais que você poderá encontrar e atingir sua persona.</p>
                    <h3 class="bs-title">Comece pela sua casa, seu site.</h3>
                    <p>O site muitas vezes é o primeiro contato com o consumidor. E assim como uma casa organizada para a visita, seu site também deve seguir a linha. Parece básico, mas ainda encontramos sites não amigáveis a quem vai visitá-lo.</p>
                    <p>Sites não responsivos, difícil navegação, informação bagunçada ou carregamento lento, são somente alguns dos grandes empecilhos que nos deparamos quando visitamos determinados sites na internet.</p>
                    <p>Se você busca aumentar e engajar sua audiência, itens como este que dificultam a vida dos usuários devem ser corrigidos imediatamente, para que depois você consiga explorar formas de ter um conteúdo atrativo, dinâmico e interativo, a ponto do visitante se sentir confortável em buscar as informações que precisa e encontrar em você a solução que ele busca.</p>
                    <p>Criar um site que busque uma melhor experiência do usuário tem sido um dos pontos principais para as equipes de marketing.</p>
                    <h3 class="bs-title">Sua empresa produz conteúdo relevante?</h3>
                    <p>O marketing de conteúdo já é tema campeão dentro das estratégias de marketing. E não podia ser diferente. Produzir conteúdo é uma das formas mais eficientes para engajar e construir uma audiência digital.</p>
                    <p>Mais de 90% da audiência online busca através dos conteúdos, sanar suas dúvidas antes de comprar determinado produto. Seja no blog ou rede social você também já deve ter buscado conteúdos que ajudem no momento da compra, e que muitas vezes acabou conhecendo determinada marca através dele.</p>
                    <p>Além de aumentar seu alcance de público, torna-se extremamente eficiente no momento de reduzir seu custo de aquisição. Sendo assim, outra motivação para entrar nas estratégias das equipes de marketing.</p>
                    <h3 class="bs-title">No que mais ajuda?</h3>
                    <p>Bom, a partir do momento que você produz conteúdo que sanem e ajudem nas dúvidas dos visitantes, você se torna referência naquele determinado assunto. Provavelmente você tenha alguns blogs preferidos que acompanha e segue periodicamente. E que talvez, em algum momento, efetuou uma compra por já se sentir confortável e com confiança naquela empresa.</p>
                    <p>Imagine você procurar sobre tendências de moda, como usar determinado produto ou trombar com algum conteúdo que faça o comparativo com os produtos que está em dúvida. A probabilidade de você comprar desse site é muito grande ou até mesmo indicá-lo. O que nos leva ao próximo tópico.</p>
                    <h3 class="bs-title">Compartilhando conteúdo</h3>
                    <p>Concorda que quanto mais pessoas compartilhar seus conteúdos, mais pessoas fora desse seu alcance conhecerão sua marca?</p>
                    <p>Só que o grande desafio é esse, como fazer com que as pessoas compartilhe voluntariamente?</p>
                    <p>Aqui entramos no resumo de tudo que comentamos até agora. É preciso conhecer a sua persona para que tenha sua proposta de valor alinhada a ela, e com isso, produzir conteúdos relevantes que a ajudem e estimulem a compartilhar com outros amigos.</p>
                    <p>Um conteúdo que fale sobre como combinar determinadas roupas para x ocasião, um vídeo de como usar determinado produto ou até mesmo aquele conteúdo de lançamento do produto y são suscetíveis a serem compartilhados. Mas saiba, não há uma fórmula pronta.</p>
                    <h3 class="bs-title">Presença online x reputação</h3>
                    <p>A presença online que a sua marca deve buscar é aquela que fique gravada na memória do seu público e construa uma reputação a partir da exposição deles.</p>
                    <p>Levam-se anos para construir uma reputação, e minutos para arruiná-la. Podemos citar casos de crise das marcas devido a problemas de campanhas consideradas racistas, casos divulgados sobre trabalho escravo ou até mesmo escândalos de corrupção de surgiram a tona pelo país.</p>
                    <p>Ao criar uma audiência digital, esse é um dos ativos que a empresa deve tomar o máximo cuidado. Tendo agilidade nas respostas, preocupação em atender da melhor forma o outro lado e sempre se renovar.</p>
                    <h3 class="bs-title">E qual o papel das redes sociais?</h3>
                    <p>As redes sociais se tornaram um dos principais canais de comunicação com as marcas. Através delas tiram-se dúvidas, realizam reclamações, compram, elogiam e detonam marcas. Ter uma gestão desse canal é fundamental para evitar crises e comprometimento da sua audiência.</p>
                    <p>As redes sociais é canal estratégico para que a sua marca possa engajar sua audiência, aumentá-la e ter sua reputação fortalecida. Muitas marcas trabalham muito bem a gestão de suas redes sociais. Podemos citar casos, como: Arezzo, Netflix, PontoFrio, Oreo, entre outras.</p>
                    <p>Se espelhar e buscar uma comunicação mais humanizada e personalizada tem sido ingredientes fundamentais para esse sucesso.</p>
                    <h3 class="bs-title">Promova sua comunicação</h3>
                    <p>Espalhe sua comunicação através dos canais de comunicação da sua empresa, expanda esses canais, promova seus conteúdos para mais pessoas. Ferramentas como Facebook Ads, Google Adwords e outros, são algo a ser explorado na busca por atingir um público maior e crescer sua audiência.</p>
